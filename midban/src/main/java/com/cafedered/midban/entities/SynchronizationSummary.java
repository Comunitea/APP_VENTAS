package com.cafedered.midban.entities;

import com.cafedered.cafedroidlitedao.annotations.Entity;
import com.cafedered.cafedroidlitedao.annotations.Id;
import com.cafedered.cafedroidlitedao.annotations.Property;
import com.cafedered.midban.annotations.RemoteProperty;

@Entity(tableName = "synchronization_summary")
public class SynchronizationSummary extends BaseEntity {

    private static final long serialVersionUID = 6178888599204805534L;

    @Id(autoIncrement = true, column = "id")
    @RemoteProperty(name = "id")
    private Long id;

    @Property(columnName = "date_synchronization")
    private String date;

    @Property(columnName = "results")
    private String result;

    @Property(columnName = "time")
    private Number timeInSeconds;

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }

    public String getResult() {
        return result;
    }

    public void setResult(String result) {
        this.result = result;
    }

    public Number getTimeInSeconds() {
        return timeInSeconds;
    }

    public void setTimeInSeconds(Number timeInSeconds) {
        this.timeInSeconds = timeInSeconds;
    }

    @Override
    public Long getId() {
        return id;
    }

    @Override
    public void setId(Long id) {
        this.id = id;
    }

}
